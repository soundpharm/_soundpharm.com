			<h1>About soundpharm</h1>
			<p>Soundpharm is an experiment in sonic arrangements. One area of research is using fractal algorithms to generate melodies and chord progressions. Feedback, sacred numbers and mathematic concepts are all major influences for the creative process. Soundpharm is essentially the aural counterpart of my visual art. </p>

			<h3>Artist history</h3>
			It all started at CCAD in 1995, making soundtracks for my video feedback collages with some toys, metal obects and a 4-track reel-to-reel. It became "The All-Monster Chorus" after a lot of experimenting with various forms of audio feedback. The first digital compositions began in Spring of 1997. More sonic experiments continued through 1999, consuming hours of cassette tape. A few live shows were played at this time, mostly as part of the Elemental scene. In 2000, <b>fractal music generators</b> like GingerBread &amp; Fmusic were discovered and a new world of sonic experimentation opened up. A few instruments were purchased, including the centerpiece of the studio, a Roland JP-8000. I collaborated and performed a couple shows with <a href="http://www.scottyboombox.com/" target="_blank">Scotty Boombox</a> around this time. In recent years the studio productions have been a mix of fractal melodies and original compositions seasoned with a little noisy chaos.</p>

