		<h1>The Short Tale of Justin Jason</h1>
		<p> 
			Born and raised in rural central Ohio by an open and supportive family 
			I reflect little of the qualities of my native small-town, Mt. Gilead. 
			Perhaps the acceptance from my parents helped to ward off the closed mindedness 
			that surrounded me growing up. Somehow I managed one day to find myself 
			contemplating the life decision of which college to attend. My creative 
			drive told me to pick art school while my rational mind told me anything but 
			art school. Either way the scholarship to Columbus College of Art &amp; Design spoke 
			for itself and I listened.
		</p>
		<p>
			Attending Columbus College of Art &amp; Design in fall of 1993 I began a course of 
			self-exploration that led me through many areas of visual communication. 
			Following my love for technical complexity I entered into the Industrial 
			Design major, only to find after a year that I missed working with pure 
			imagery too much. Switching to Fine Arts I explored various mediums and 
			began to hone my own personal aesthetic. Watercolor and photography became 
			my mediums of choice and I soon changed major again to Fine Arts - Photography. 
			In this major I was exposed to several processes that really took hold 
			of my creative drive. Animation, digital imagery and interactivity 
			opened up a whole new door of expression for me and soon filled up almost 
			every waking moment. Realizing that with the digital revolution I could 
			make a living and be creative I soon changed my major to Time-Based Media Studies. 
		</p>
		<p>
			Graduating in Dec. of '97 with a Bachelor of Fine Arts, I soon found work in my field 
			at Painted Rain Interactive. At PRI, I quickly moved up the ranks from 
			Creative Production to Senior CD-ROM Developer and finally to Art Director. 
			The time spent at Painted Rain allowed me to sharpen my interactive development 
			skills and to learn the ins and outs of web development. Designs for corporate 
			identity, intuitive navigation and tech savvy where all implemented 
			regularly through PhotoShop, Flash, DreamWeaver and Director. 
			I often found myself meeting with clients and potential clients, working 
			to visualize their needs and message. I also found that money isn't all 
			it takes to run a new business. The naive court reporters who started 
			the company had little idea of how to profit from new media. 
		</p>
		<p>
			As PRI crumbled I soon found my way to <a href="http://www.digiknow.com" target="_blank">DigiKnow</a>, 
			a solid company started in '93 by an ad agency (at least!). I have found bliss working at 
			DigiKnow, it's simply the greatest work environment I could hope for! Headquartered in Cleveland, Ohio, 
			DigiKnow also has satellite offices in Columbus and Dallas, Texas. I work in the Columbus office
			located in the Smith Bros. Hardware building (a building which I spent many days exploring prior 
			to it's renovation in 1997).
		</p>
		<p>
			Outside of DigiKnow I have taken on a few freelance website designs. Great friends of mine,
			Carrie and Eric Frey, commissioned me to design a site for their growing company, 
			<a href="http://www.jawnetworks.com/" target="_blank">Jaw Networks</a>. Working with a provided logo
			and color scheme I developed the first site design in the Spring of '02. In early Spring of '03, an 
			updated version of the site was launched with a modified page layout throughout the site. 
		<p>
			In 2001 I designed a site for another friend who is a tattooist, Kat Dearwester aka <a href="http://www.f14hellkat.com" target="_blank">f-14 HellKat</a>. 
			The site has since been redesigned by someone else though.
		</p>
		<p>
			I created a snack food logo, wine ad and an online CD store banner in 2003. 
		</p>
		<p>
			Outside of work I can be found on my <a href="http://www.skatepharm.com/graphics/fall05/11-25-05-JJ-newride.jpg" target="_blank">roller plank</a> (AKA <a href="http://www.fivepointsskates.com/decks.htm" target="_blank">skateboard</a>).
			I also have a constant urge to create music. Check out the "<a href="/listen/">Listen</a>" 
			section for examples of my music.
		</p>
		<p> 
			I bought a <a href="http://www.skatepharm.com/graphics/home/dublin_gothic1.jpg" target="_blank">house</a> near Columbus with my beautiful wife <a href="http://www.skatepharm.com/graphics/winter06/03-26-06-NJ-hip.jpg" target="_blank">Nikki</a>, 
			and I love spending time adding our personal touches to it. We've <a href="http://www.skatepharm.com/graphics/spring04/4-19-04-housefront.jpg" target="_blank">remodeled</a> almost each 
			major room <a href="http://www.skatepharm.com/graphics/spring04/4-1-04-bathroom-finished.jpg" target="_blank">one way</a> or another.
		</p>
		<p>
			Nikki and I love to skateboard and often visit the great concrete temples such as 
			the <a href="http://www.athensskatepark.com/" target="_blank">Athens</a> and 
			<a href="http://www.skateboardpark.com/skateboardpark/viewpark.asp?ID=540" target="_blank">Louisville</a> 
			skateparks. We have been to <a href="http://www.skatepharm.com/graphics/fall05/10-14-05-NJ-50-50crnr.jpg" target="_blank">Nashville</a> to skate the infamous 
			"<a href="http://www.concretedisciples.com/cd_skate/tennessee/nashville/nashville.php" target="_blank">General</a>". During winter break in 2005 we made a pilgrimage out west and skated Fontana, Upland, Chino and Glendale. The culmination of the trip was when we stumbled upon the original Jeff Ho shop just recently re-opened in Venice. 
		</p>
		<p>
			For our fifth anniversary we spent a week on California. After a few days in the L.A. area we headed up to the Sierra Nevada mountain range in northern CA. We fell in love with the Mammoth Lake area and can't wait to go back. We ended up extending our stay so that we could skate another day at the huge <a href="http://www.skatepharm.com/spring07.php" target="_blank">Mammoth Lake skatepark</a>. Lots of great memories were made during this trip. Many were gathered on our day long excursion to Yosemite National Park. There's nothing like driving across the Tioga Pass. Check out <a href="http://www.flickr.com/photos/soundpharm/" target="_blank">our photos</a> on Flickr.
		</p>
		<p>
			We upgraded to a digital SLR camera this summer (2006) and have been using it often. So much I put together a website dedicated to our photos (primarily skating photos). You can see them here: <a href="http://www.skatepharm.com" target="_blank">www.skatepharm.com</a>. You can also find our other photos on our Flickr page: <a href="http://www.flickr.com/soundpharm/" target="_blank">www.flickr.com/soundpharm/</a>.
		</p>
		<p>
			For Halloween 2003, we took a Frank Loyd Wright trip to Chicago's Oak Park to see about 10 of 
			Wright's houses. We were blessed to see yet another Frank Gehry building in progress on our trip. 
			The first time was the <a href="http://www.guggenheim.org/exhibitions/past_exhibitions/gehry/lewis_25.html" target="_blank">Peter B. 
			Lewis Building</a>, 
			<a href="http://www.morttucker.com/weatherhead/index.htm" target="_blank">Case Western Reserve 
			University</a>, <a href="http://www.cwru.edu/pubaff/univcomm/vnr/spring01/gehry.htm" target="_blank">Cleveland</a>, and the most recent was the <a href="http://www.makearchitecture.com/blogarchives/000034.html" target="_blank">Millennium Park 
			Music Pavilion</a>.
		</p>
		
		<p>I like to travel as often as possible and 
		have been to Europe twice. The first trip was to Paris and London in 1998, 
		and the second was with Nikki to Paris, Brussels, Rotterdam and Amsterdam in 2000. I've also found New England 
		to be a great place to get away from Ohio to, particularly Vermont. Nikki and I 
		liked the area so much we even got married in the White Mountains of New Hampshire. Seeking out 
		art and enlightenment of all kinds, I plan to continue travels as often as my wallet will 
		allow. 
	</p>
