			<h1>Dusting off the website</h1>
			<p>In late August I found out that my proposed piece for Por Vida V had been accepted. I immediately began working out the details of the sketches I had completed earlier. This wasn't just a painting, it was going to be seven and they were all going to be connected together somehow. Many late nights came and went and just a few days before the deadline we received a week extension. That was a great weight off my back and it allowed me to give some extra attention to the remaining details.Easily the most personal piece of art I have ever created. I'm very happy with how it came together and manifested my vision of it.</p>
